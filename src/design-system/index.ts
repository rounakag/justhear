// Utility functions
export const createVariantClass = (
  baseClass: string,
  variants: Record<string, Record<string, string>>,
  props: Record<string, any>
): string => {
  let classes = baseClass;
  
  Object.entries(variants).forEach(([variantName, variantOptions]) => {
    const value = props[variantName];
    if (value && variantOptions[value]) {
      classes += ` ${variantOptions[value]}`;
    }
  });
  
  return classes;
};

